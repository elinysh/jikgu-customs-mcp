TOPIC_KEYS = tuple(TOPICS.keys())


def explain(topic: str) -> str:
    key = (topic or "").strip().lower()
    if key not in TOPICS:
        allowed = ", ".join(TOPIC_KEYS)
        raise ValueError(f"지원하지 않는 topic 입니다: {topic!r}. 허용 값: {allowed}")
    return TOPICS[key]
